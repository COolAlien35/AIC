# aic/agents/comms_agent.py
"""
Communications & Postmortem Agent — auto-generates full incident documentation.

At episode end, generates:
  - Executive Summary (2 sentences, business language)
  - Engineering Summary (technical timeline)
  - Customer-Safe Summary (PR-safe version)
  - Postmortem Draft (full structured doc)
  - Remediation Checklist
  - Prevention Recommendations

Template-based — no LLM required. Rich variable substitution gives
realistic, scenario-specific output every time.
"""
from __future__ import annotations

import json
from dataclasses import dataclass, field
from datetime import datetime
from typing import Optional


@dataclass
class TimelineEntry:
    step: int
    event: str
    agent: str
    impact: str


@dataclass
class PostmortemReport:
    """Full postmortem report for one episode."""
    generated_at: str
    scenario_name: str
    root_cause: str
    severity: str
    mttr_steps: int
    final_health: float
    sla_met: bool

    executive_summary: str
    engineering_summary: str
    customer_safe_summary: str

    timeline: list[TimelineEntry]
    contributing_factors: list[str]
    key_decisions: list[str]
    debate_highlights: list[str]

    remediation_checklist: list[str]
    prevention_recommendations: list[str]

    revenue_impact_usd: float
    users_protected: int
    slo_impact: str

    def to_markdown(self) -> str:
        """Render postmortem as a formatted markdown document."""
        sla_status = "✅ Met" if self.sla_met else "❌ Breached"
        health_pct = f"{self.final_health * 100:.1f}%"

        lines = [
            f"# 🚨 Incident Postmortem: {self.scenario_name}",
            f"> Generated by AIC Comms Agent at {self.generated_at}",
            "",
            "## 📋 Executive Summary",
            self.executive_summary,
            "",
            "## 👥 Customer Communication",
            self.customer_safe_summary,
            "",
            "## ⚙️ Engineering Summary",
            self.engineering_summary,
            "",
            "## 📊 Impact Metrics",
            f"| Metric | Value |",
            f"|--------|-------|",
            f"| Severity | **{self.severity}** |",
            f"| MTTR | **{self.mttr_steps} steps** |",
            f"| SLA | {sla_status} |",
            f"| Final System Health | {health_pct} |",
            f"| Revenue Protected | **${self.revenue_impact_usd:,.0f}** |",
            f"| Users Protected | {self.users_protected:,} |",
            f"| SLO Impact | {self.slo_impact} |",
            "",
            "## 🔍 Root Cause",
            f"> {self.root_cause}",
            "",
            "## 📅 Incident Timeline",
        ]

        for t in self.timeline:
            lines.append(f"- **Step {t.step}** [{t.agent}]: {t.event} — *{t.impact}*")

        lines += [
            "",
            "## ⚠️ Contributing Factors",
        ]
        for cf in self.contributing_factors:
            lines.append(f"- {cf}")

        lines += [
            "",
            "## 🧠 Key Decisions (AIC Reasoning)",
        ]
        for kd in self.key_decisions:
            lines.append(f"- {kd}")

        if self.debate_highlights:
            lines += ["", "## 🗣️ Agent Debate Highlights"]
            for dh in self.debate_highlights:
                lines.append(f"> {dh}")

        lines += [
            "",
            "## ✅ Remediation Checklist",
        ]
        for item in self.remediation_checklist:
            lines.append(f"- [ ] {item}")

        lines += [
            "",
            "## 🛡️ Prevention Recommendations",
        ]
        for rec in self.prevention_recommendations:
            lines.append(f"- {rec}")

        lines += [
            "",
            "---",
            "*Auto-generated by AIC Comms Agent — Adaptive Incident Choreographer*",
        ]

        return "\n".join(lines)

    def to_dict(self) -> dict:
        return {
            "generated_at": self.generated_at,
            "scenario_name": self.scenario_name,
            "root_cause": self.root_cause,
            "severity": self.severity,
            "mttr_steps": self.mttr_steps,
            "final_health": self.final_health,
            "sla_met": self.sla_met,
            "executive_summary": self.executive_summary,
            "engineering_summary": self.engineering_summary,
            "customer_safe_summary": self.customer_safe_summary,
            "timeline": [{"step": t.step, "event": t.event, "agent": t.agent, "impact": t.impact} for t in self.timeline],
            "contributing_factors": self.contributing_factors,
            "key_decisions": self.key_decisions,
            "debate_highlights": self.debate_highlights,
            "remediation_checklist": self.remediation_checklist,
            "prevention_recommendations": self.prevention_recommendations,
            "revenue_impact_usd": self.revenue_impact_usd,
            "users_protected": self.users_protected,
            "slo_impact": self.slo_impact,
        }


