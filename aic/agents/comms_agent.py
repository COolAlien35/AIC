class CommsAgent:
    """
    Generates structured incident communications at episode end.

    Input: episode trajectory (list of step dicts from run_episode).
    Output: PostmortemReport with all sections populated.
    """

    REVENUE_PER_STEP = 5000.0   # $ per healthy step (matches benchmark_suite)
    BASE_USERS = 50_000

    def generate_postmortem(
        self,
        scenario_name: str,
        episode_traces: list[dict],
        final_health: float,
        mttr_steps: int,
        root_cause_hypothesis: Optional[dict] = None,
        business_severity: str = "P2",
        debate_rounds: Optional[list[dict]] = None,
        total_revenue_saved: float = 0.0,
    ) -> PostmortemReport:
        """
        Generate a full postmortem report from episode data.

        Args:
            scenario_name: Name of the scenario (matches SCENARIO_REGISTRY).
            episode_traces: List of step dicts from trajectory.
            final_health: Final episode health score [0, 1].
            mttr_steps: Steps until first health > 0.5.
            root_cause_hypothesis: Bayesian root cause dict from analyst.
            business_severity: P1-P4 business severity level.
            debate_rounds: List of debate round dicts (optional).
            total_revenue_saved: Revenue protected during episode.
        """
        now = datetime.now(tz=__import__('datetime').timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
        sla_met = final_health > 0.5

        # Root cause
        root_cause = _ROOT_CAUSE_TEMPLATES.get(
            scenario_name,
            f"Cascading failure originating in {scenario_name.lower()} service tier.",
        )
        if root_cause_hypothesis:
            confidence = root_cause_hypothesis.get("confidence", 0)
            analyst_name = root_cause_hypothesis.get("scenario_name", scenario_name)
            root_cause = (
                f"[AIC RCA confidence: {confidence:.0%}] {root_cause} "
                f"Root Cause Analyst identified '{analyst_name}' as primary hypothesis."
            )

        # Timeline from traces
        timeline = self._build_timeline(episode_traces, scenario_name)

        # Key decisions
        key_decisions = self._extract_key_decisions(episode_traces)

        # Debate highlights
        debate_highlights = self._extract_debate_highlights(debate_rounds or [])

        # Contributing factors
        contributing_factors = self._build_contributing_factors(episode_traces, scenario_name)

        # Summaries
        health_pct = int(final_health * 100)
        mttr_desc = f"{mttr_steps} steps" if mttr_steps < 20 else "not achieved within SLA window"
        sla_str = "successfully maintained SLA compliance" if sla_met else "experienced SLA breach"

        executive_summary = (
            f"On {now}, AIC autonomously detected and responded to a '{scenario_name}' incident "
            f"(severity {business_severity}), achieving {health_pct}% system health recovery "
            f"with MTTR of {mttr_desc}. "
            f"The autonomous command system {sla_str}, protecting an estimated "
            f"${total_revenue_saved:,.0f} in revenue."
        )

        engineering_summary = (
            f"Incident: {scenario_name} (severity {business_severity}). "
            f"Detection: Step 0 (immediate). "
            f"Root cause confirmed by Bayesian analyst at step {min(3, len(episode_traces))}. "
            f"First remediation action applied at step 1. "
            f"MTTR: {mttr_desc}. "
            f"Orchestrator executed {len(episode_traces)} decision cycles, "
            f"with {len(key_decisions)} high-impact decisions. "
            f"Recovery Verifier blocked {sum(1 for t in episode_traces if t.get('trace', {}).get('verifier_report', {}) and not t.get('trace', {}).get('verifier_report', {}).get('approved', True))} "
            f"unsafe actions. Final system health: {health_pct}%."
        )

        customer_safe_summary = (
            f"We experienced a service disruption on {now}. "
            f"Our automated incident response system detected the issue immediately "
            f"and began remediation. "
            + ("Service has been fully restored." if sla_met else
               f"Service was partially degraded; full restoration is in progress.")
            + " We apologize for any inconvenience and are implementing preventative "
            f"measures to avoid recurrence."
        )

        # SLO impact
        slo_impact = (
            "Minor SLO impact — within error budget" if final_health > 0.8 else
            "Moderate SLO breach — error budget partially consumed" if final_health > 0.5 else
            "Significant SLO breach — error budget exhausted"
        )

        users_protected = int(min(self.BASE_USERS, self.BASE_USERS * final_health))

        remediation_checklist = _REMEDIATION_BY_SCENARIO.get(
            scenario_name, ["Review incident logs", "Patch root cause", "Validate monitoring"]
        )
        prevention_recommendations = _PREVENTION_BY_SCENARIO.get(
            scenario_name, ["Implement additional monitoring", "Conduct postmortem review"]
        )

        return PostmortemReport(
            generated_at=now,
            scenario_name=scenario_name,
            root_cause=root_cause,
            severity=business_severity,
            mttr_steps=mttr_steps,
            final_health=final_health,
            sla_met=sla_met,
            executive_summary=executive_summary,
            engineering_summary=engineering_summary,
            customer_safe_summary=customer_safe_summary,
            timeline=timeline,
            contributing_factors=contributing_factors,
            key_decisions=key_decisions,
            debate_highlights=debate_highlights,
            remediation_checklist=remediation_checklist,
            prevention_recommendations=prevention_recommendations,
            revenue_impact_usd=total_revenue_saved,
            users_protected=users_protected,
            slo_impact=slo_impact,
        )

    def _build_timeline(self, traces: list[dict], scenario_name: str) -> list[TimelineEntry]:
        entries = [
            TimelineEntry(
                step=0,
                event=f"Incident detected: {scenario_name}",
                agent="AIC System",
                impact="Automated monitoring triggered incident response",
            )
        ]
        for step_data in traces[:10]:  # Summarise first 10 steps
            step = step_data.get("step", 0)
            trace = step_data.get("trace", {})
            if not trace:
                continue
            action = trace.get("action_taken", "")[:80]
            agent = trace.get("followed_agent", "Orchestrator")
            override = trace.get("override_applied", False)
            impact_note = "(override applied)" if override else "(trust-selected)"
            entries.append(TimelineEntry(
                step=step,
                event=action,
                agent=agent,
                impact=impact_note,
            ))
        return entries

    def _extract_key_decisions(self, traces: list[dict]) -> list[str]:
        decisions = []
        for step_data in traces:
            trace = step_data.get("trace", {})
            if not trace:
                continue
            if trace.get("override_applied"):
                reason = trace.get("override_reason", "trust threshold breach")
                decisions.append(f"Step {trace.get('step', '?')}: Override applied — {reason[:100]}")
            verifier = trace.get("verifier_report", {})
            if verifier and not verifier.get("approved", True):
                decisions.append(
                    f"Step {trace.get('step', '?')}: Safety Verifier blocked unsafe action "
                    f"(risk={verifier.get('risk_score', 0):.2f})"
                )
            if (trace.get("root_cause_hypothesis") or {}).get("confidence", 0) > 0.7:
                hyp = trace["root_cause_hypothesis"]
                decisions.append(
                    f"Step {trace.get('step', '?')}: Root cause confirmed — "
                    f"'{hyp['scenario_name']}' (confidence={hyp['confidence']:.0%})"
                )
        return decisions[:6] if decisions else ["Autonomous orchestrator managed all decisions within policy."]

    def _extract_debate_highlights(self, debate_rounds: list[dict]) -> list[str]:
        highlights = []
        for r in debate_rounds:
            for crit in r.get("criticisms", [])[:1]:
                highlights.append(crit.get("text", "")[:150])
            for veto in r.get("security_vetoes", [])[:1]:
                highlights.append(f"🔒 Security veto: {veto.get('reason', '')[:120]}")
            if r.get("debate_changed_selection"):
                highlights.append("⚠️ Debate altered final agent selection this step.")
        return highlights[:4]

    def _build_contributing_factors(self, traces: list[dict], scenario_name: str) -> list[str]:
        factors = [f"Primary fault: {scenario_name} incident pattern"]
        has_drift = any(
            t.get("trace", {}).get("schema_drift_detected") for t in traces
        )
        if has_drift:
            factors.append("Telemetry schema drift detected mid-incident, complicating diagnosis")
        has_override = any(
            t.get("trace", {}).get("override_applied") for t in traces
        )
        if has_override:
            factors.append("Adversarial recommendations required trust-based override")
        factors.append("Multi-metric cascading effect across service boundaries")
        factors.append("Progressive fault drift compounded initial fault vector")
        return factors
